/* ==================================================================
 * GPIO Initialization for LCD
 *
 * STM32F103 → STM32F407 adaptation:
 *   - GPIO_InitTypeDef structure differs (F4 uses Alternate, Pull, Speed fields)
 *   - RCC clock enable uses __HAL_RCC_GPIOx_CLK_ENABLE()
 *   - JTAG disable uses __HAL_RCC_AFIO_CLK_ENABLE() + __HAL_AFIO_REMAP_SWJ_NOJTAG()
 *     but for F4, we use __HAL_AFIO_REMAP_SWJ_NOJTAG (or GPIO_PinRemapConfig equivalent)
 *
 * Pin map (all available on STM32F407VET6 LQFP-100):
 *   PB12 - LCD_CS   (output)
 *   PB13 - LCD_SCL  (output)
 *   PB15 - LCD_SDA  (output)
 *   PC5  - LCD_RS   (Data/Command, output)
 *   PB14 - LCD_RST  (Hardware Reset, output)
 *   PB1  - LCD_PD/BL (Backlight, output)
 *   PB0-15 - LCD data bus (output, for parallel modes)
 *   PC0-2 - Buttons (input, pull-up)
 *   PC9,11- TP / LCD SDO (input)
 *   PA0,2,8,14,15 - TP pins (output/input)
 * ================================================================== */
void LCD_GPIOInit(void)
{
    GPIO_InitTypeDef GPIO_InitStruct = {0};

    /* Enable GPIO clocks (PORTD not used - all LCD pins on PORTA/B/C) */
    __HAL_RCC_GPIOA_CLK_ENABLE();
    __HAL_RCC_GPIOB_CLK_ENABLE();
    __HAL_RCC_GPIOC_CLK_ENABLE();

    /* On STM32F4, JTAG is disabled automatically when JTAG pins (PA15, PB3, PB4)
       are reconfigured as GPIO via HAL_GPIO_Init() below. SWD on PA13/PA14 is preserved.
       CRITICAL: PA14 (SWDCLK) and PA13 (SWDIO) must NEVER be reconfigured as GPIO. */

    /* ===== PORTC Outputs: RS(PC5) ===== */
    /* (CS, SCL, SDA moved to PORTB; RST moved to PB14) */
    GPIO_InitStruct.Pin = GPIO_PIN_5;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_PULLUP;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_MEDIUM;
    HAL_GPIO_Init(GPIOC, &GPIO_InitStruct);
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_5, GPIO_PIN_SET);

    /* ===== PORTB Outputs: CS(PB12), SCL(PB13), SDA(PB15), RST(PB14), BLK(PB1) ===== */
    GPIO_InitStruct.Pin = GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_15 | GPIO_PIN_14 | GPIO_PIN_1;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_PULLUP;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_MEDIUM;
    HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);

    /* Set initial state: RST(PB14) high, CS(PB12) high, SCL(PB13) high, SDA(PB15) high, BLK(PB1) high */
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_14 | GPIO_PIN_12 | GPIO_PIN_13 | GPIO_PIN_15 | GPIO_PIN_1, GPIO_PIN_SET);

    /* ===== PORTC Inputs: PC0(KEY_UP), PC1(KEY_STEP), PC2(ENTER), PC9(SDO), PC11(TP_DOUT) ===== */
    GPIO_InitStruct.Pin = GPIO_PIN_0 | GPIO_PIN_1 | GPIO_PIN_2 | GPIO_PIN_9 | GPIO_PIN_11;
    GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
    GPIO_InitStruct.Pull = GPIO_PULLUP;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_LOW;
    HAL_GPIO_Init(GPIOC, &GPIO_InitStruct);

    /* ===== PORTA: PA0, PA2(TP_DCLK), PA15(TP_CS) as outputs ===== */
    /* NOTE: PA14 is SWDCLK on STM32F407 — must NEVER be configured as GPIO output */
    GPIO_InitStruct.Pin = GPIO_PIN_0 | GPIO_PIN_2 | GPIO_PIN_15;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

    /* PA8(TP_PENIRQ) as input */
    GPIO_InitStruct.Pin = GPIO_PIN_8;
    GPIO_InitStruct.Mode = GPIO_MODE_INPUT;
    GPIO_InitStruct.Pull = GPIO_PULLUP;
    HAL_GPIO_Init(GPIOA, &GPIO_InitStruct);

    /* ===== PORTD: unused (BLK moved to PB1) ===== */

    /* ===== PORTB data bus (parallel mode only): PB0, PB2-PB11 ===== */
    /* PB1(BLK), PB12(CS), PB13(SCL), PB14(RST), PB15(SDA) already initialized above */
    GPIO_InitStruct.Pin = GPIO_PIN_0 | GPIO_PIN_2 | GPIO_PIN_3 | GPIO_PIN_4 |
                          GPIO_PIN_5 | GPIO_PIN_6 | GPIO_PIN_7 | GPIO_PIN_8 |
                          GPIO_PIN_9 | GPIO_PIN_10 | GPIO_PIN_11;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(GPIOB, &GPIO_InitStruct);
    HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0 | GPIO_PIN_2 | GPIO_PIN_3 | GPIO_PIN_4 |
                              GPIO_PIN_5 | GPIO_PIN_6 | GPIO_PIN_7 | GPIO_PIN_8 |
                              GPIO_PIN_9 | GPIO_PIN_10 | GPIO_PIN_11, GPIO_PIN_SET);

    /* ===== PORTC additional: PC10(TP_DIN) as output ===== */
    GPIO_InitStruct.Pin = GPIO_PIN_10;
    GPIO_InitStruct.Mode = GPIO_MODE_OUTPUT_PP;
    GPIO_InitStruct.Pull = GPIO_NOPULL;
    GPIO_InitStruct.Speed = GPIO_SPEED_FREQ_HIGH;
    HAL_GPIO_Init(GPIOC, &GPIO_InitStruct);
}

/* ==================================================================
 * Initialize LCD configuration from compile-time defines
 * ================================================================== */
void LCD_CONFIG_INIT(void)
{
    lcdconfig.col = COL;
    lcdconfig.row = ROW;
    lcdconfig.col_pre = COL_Pre;
    lcdconfig.row_pre = ROW_Pre;

#ifdef LCD_MCU8
    lcdconfig.lcd_interface = MCU8;
#endif
#ifdef LCD_MCU16
    lcdconfig.lcd_interface = MCU16;
#endif
#ifdef LCD_MCUH8
    lcdconfig.lcd_interface = MCUH8;
#endif
#ifdef LCD_SPI4Line
    lcdconfig.lcd_interface = SPI4Line;
#endif
#ifdef LCD_SPI3Line
    lcdconfig.lcd_interface = SPI3Line;
#endif
#ifdef LCD_SPI3Line2
    lcdconfig.lcd_interface = SPI3Line2;
#endif
#ifdef LCD_SPI4Line_CS_D8
    lcdconfig.lcd_interface = SPI4Line_CS_D8;
#endif

    lcdconfig.delay_time = Delay_Time;

#ifdef commdata16
    lcdconfig.is_commdata16 = 1;
#else
    lcdconfig.is_commdata16 = 0;
#endif

#ifdef TEST_STAND_H9B
    lcdconfig.pic_bytes = (COL * ROW) * 3;
#else
    lcdconfig.pic_bytes = (COL * ROW) << 1;
#endif
}

/* ==================================================================
 * Hardware Reset Sequence
 *   RST High → delay 50ms → RST Low → delay 200ms → RST High → delay 200ms
 * ================================================================== */
void LCD_RESET(void)
{
    LCD_RST_SET();
    delay_ms(50);
    LCD_RST_CLR();
    delay_ms(200);
    LCD_RST_SET();
    delay_ms(200);
}

/* ==================================================================
 * Full LCD Initialization
 *   GPIO → Config → Reset → IC Init
 * ================================================================== */
void LCD_Init(void)
{
    LCD_GPIOInit();
    LCD_CONFIG_INIT();
    LCD_RESET();
    LCD_IC_Init();
}

/* ==================================================================
 * Low-level pixel write
 * ================================================================== */
void WriteOneDot(unsigned int color)
{
    LCD_CS_CLR();
    LCD_RD_SET();
    LCD_RS_SET();
    SendData(color);
    LCD_CS_SET();
}

void WriteDispData(unsigned int color1, unsigned int color2)
{
    SendData(color1);
    SendData(color2);
}

/* ==================================================================
 * Full-screen color fill
 * ================================================================== */
void DispColor(unsigned int color)
{
    unsigned int i, j;

    BlockWrite(COL_Pre, COL + COL_Pre - 1, ROW_Pre, ROW + ROW_Pre - 1);

    LCD_CS_CLR();
    LCD_RD_SET();
    LCD_RS_SET();

    for (i = 0; i < ROW; i++) {
        for (j = 0; j < COL; j++) {
            SendData(color);
        }
    }

    LCD_CS_SET();
}

/* ==================================================================
 * Display vertical color bands (8 bands)
 * ================================================================== */
void DispBand(void)
{
    int i, j, k;
    unsigned int color[8] = {0xF800, 0xF800, 0x07E0, 0x07E0,
                             0x001F, 0x001F, 0xFFFF, 0xFFFF};

    BlockWrite(COL_Pre, COL + COL_Pre - 1, ROW_Pre, ROW + ROW_Pre - 1);

    LCD_CS_CLR();
    LCD_RD_SET();
    LCD_RS_SET();

    for (i = 0; i < 8; i++) {
        for (j = 0; j < ROW / 8; j++) {
            for (k = 0; k < COL; k++) {
                SendData(color[i]);
            }
        }
    }

    /* Fill remaining rows */
    for (j = 0; j < (int)(ROW % 8); j++) {
        for (k = 0; k < COL; k++) {
            SendData(color[7]);
        }
    }

    LCD_CS_SET();
}

/* ==================================================================
 * Display colored frame (border)
 * ================================================================== */
void DispFrame(void)
{
    unsigned int i, j;

    BlockWrite(COL_Pre, COL + COL_Pre - 1, ROW_Pre, ROW + ROW_Pre - 1);

    LCD_CS_CLR();
    LCD_RD_SET();
    LCD_RS_SET();


    /* Top line: Red - White... - Blue */
    SendData(0xF800);
    for (i = 0; i < COL - 2; i++) { SendData(0xFFFF); }
    SendData(0x001F);

    /* Middle rows: Red border - Black fill - Blue border */
    for (j = 0; j < ROW - 2; j++) {
        SendData(0xF800);
        for (i = 0; i < COL - 2; i++) { SendData(0x0000); }
        SendData(0x001F);
    }

    /* Bottom line: Red - White... - Blue */
    SendData(0xF800);
    for (i = 0; i < COL - 2; i++) { SendData(0xFFFF); }
    SendData(0x001F);

    LCD_CS_SET();
}

/* ==================================================================
 * 16-level horizontal grayscale pattern
 * ================================================================== */
void DispGrayHor16(void)
{
    int i, j, k;

    BlockWrite(COL_Pre, COL + COL_Pre - 1, ROW_Pre, ROW + ROW_Pre - 1);

    LCD_CS_CLR();
    LCD_RD_SET();
    LCD_RS_SET();

    for (i = 0; i < ROW; i++) {
        for (j = 0; j < (int)(COL % 16); j++) {
            SendData(0);
        }
        for (j = 0; j < 16; j++) {
            for (k = 0; k < COL / 16; k++) {
                SendData(((((j * 2) << 3) | ((j * 4) >> 3)) << 8) |
                         (((j * 4) << 5) | (j * 2)));
            }
        }
    }

    LCD_CS_SET();
}

/* ==================================================================
 * 32-level horizontal grayscale pattern
 * ================================================================== */
void DispGrayHor32(void)
{
    int i, j, k;

    BlockWrite(COL_Pre, COL + COL_Pre - 1, ROW_Pre, ROW + ROW_Pre - 1);

    LCD_CS_CLR();
    LCD_RD_SET();
    LCD_RS_SET();

    for (i = 0; i < ROW; i++) {
        for (j = 0; j < (int)(COL % 32); j++) {
            SendData(0);
        }
        for (j = 0; j < 32; j++) {
            for (k = 0; k < COL / 32; k++) {
                SendData(((((j << 3) | ((j * 2) >> 3))) << 8) |
                         (((j * 2) << 5) | j));
            }
        }
    }

    LCD_CS_SET();
}

/* ==================================================================
 * Pixel drawing (for touch panel test)
 * ================================================================== */
void PutPixel(unsigned int x, unsigned int y, unsigned int color)
{
    int i;

    BlockWrite(x, x + 3, y, y + 3);

    LCD_CS_CLR();
    LCD_RD_SET();
    LCD_RS_SET();

    for (i = 0; i < 16; i++) {
        DATAOUT(color);
        LCD_WR_CLR();
        LCD_WR_SET();
    }

    LCD_CS_SET();
}

void DrawLine(unsigned int Xstart, unsigned int Xend,
              unsigned int Ystart, unsigned int Yend, unsigned int color)
{
    unsigned int i, j;

    BlockWrite(Xstart, Xend, Ystart, Yend);

    for (i = Ystart; i < Yend + 1; i++) {
        for (j = Xstart; j < Xend + 1; j++) {
            WriteOneDot(color);
        }
    }
}

void DrawGird(unsigned int color)
{
    unsigned int i;

    for (i = 15; i < ROW - 1; i = i + 16) {
        DrawLine(0, COL - 1, i, i, color);
    }

    for (i = 15; i < COL - 1; i = i + 16) {
        DrawLine(i, i, 0, ROW - 1, color);
    }
}

/* ==================================================================
 * Cursor and point drawing
 * ================================================================== */
void LCD_SetCursor(uint16_t Xpos, uint16_t Ypos)
{
    BlockWrite(Xpos, Xpos + 1, Ypos, Ypos + 1);
}

void LCD_Fast_DrawPoint(uint16_t x, uint16_t y, uint16_t color)
{
    LCD_SetCursor(x, y);
    WriteOneDot(color);
}

void LCD_DrawPoint(uint16_t x, uint16_t y)
{
    LCD_SetCursor(x, y);
    WriteOneDot(POINT_COLOR);
}

